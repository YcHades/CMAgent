

def greet(content: str) -> str:
    """
    A simple greeting tool that returns a greeting message.

    Args:
        content: The content to include in the greeting.
    """
    return f"Hello! You said: {content}"


def intro(content: str) -> str:
    """
    A simple introduction tool that returns an introduction message.

    Args:
        content: The content to include in the introduction.
    """
    return f"Here's an introduction: {content}"